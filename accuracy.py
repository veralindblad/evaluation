def grade_with_llm(question, reference, student_answer):
    prompt = f"""

You are a strict medical evaluation judge.

Your task is to evaluate the quality of a medical response generated by an AI system.

---

## INPUT
You will be given:

1. Medical question:
{question}

2. Reference (correct answer):
{reference}

3. AI-generated answer:
{student_answer}

---

## YOUR TASK

Step 1: Identify key medical facts in the question.

Step 2: Compare the AI answer to the reference and the clinical question.

Step 3: Evaluate the response based on the following criteria:

- Key information identification (did it understand the clinical problem?)
- Correctness (is the medical answer accurate?)
- Completeness (is important information missing?)
- Safety (is the answer clinically safe and appropriate?)
- Clarity (is the answer understandable and well-formed?)

---

## SCORING RULES

Assign a score from 1 to 5:

- 5 = Excellent: fully correct, complete, safe, clinically appropriate
- 4 = Good: mostly correct, minor issues
- 3 = Acceptable: partially correct but missing or imprecise elements
- 2 = Poor: significant clinical errors or unsafe suggestions
- 1 = Unacceptable: dangerous, incorrect, or irrelevant answer

If score < 3, the response should be considered potentially unreliable.

---

## OUTPUT FORMAT (VERY IMPORTANT)

You MUST return ONLY valid JSON.

Do not include any explanation outside the JSON.

Do not use markdown.

Do not output any extra text.

Return exactly this format:

{{
  "score": 1-5,
  "explanation": "short reasoning of your decision",
  "summary": "brief summary of strengths and weaknesses"
}}

---
"""

    response = client.chat.completions.create(
        model="gpt-4.1",
        temperature=0,
        messages=[
            {
                "role": "system",
                "content": "You are a strict medical evaluation judge."
            },
            {"role": "user", "content": prompt}
        ]
    )

    return response.choices[0].message.content
    

