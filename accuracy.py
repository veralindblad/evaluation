# -----------------------


def run_evaluation(file_in, file_out):
    questions = load_questions_from_file(file_in)
    initialize_csv_if_needed(file_out)

    for q in questions:
        qid = q["question_id"]
        question = q["question"]
        reference = q["reference"]

        student_answer = q.get("ai_answer", "")

        print(f"Kör fråga {qid}")

        # -----------------------
        # EMBEDDING (1 gång)
        # -----------------------
        emb_score = grade_with_embeddings(reference, student_answer)["similarity"]

        # -----------------------
        # RERANKER (1 gång)
        # -----------------------
        rerank_score = grade_with_rerank(question, student_answer)

        # -----------------------
        # LLM JUDGE (5 gånger)
        # -----------------------
        llm_scores = []
        llm_explanations = []
        llm_summaries = []

        for i in range(5):
            try:
                result = grade_with_llm(question, reference, student_answer)

                parsed = json.loads(result)

                score = parsed.get("score", -1)
                explanation = parsed.get("explanation", "")
                summary = parsed.get("summary", "")

                llm_scores.append(score)
                llm_explanations.append(explanation)
                llm_summaries.append(summary)

            except Exception as e:
                llm_scores.append(-1)
                llm_explanations.append("")
                llm_summaries.append("")

        # -----------------------
        # PRINT (valfritt men bra för debug)
        # -----------------------
        print(f"\n--- Q{qid} ---")
        print("Embedding:", emb_score)
        print("Reranker:", rerank_score)

        for i in range(5):
            print(f"\nLLM run {i+1}")
            print("Score:", llm_scores[i])
            print("Explanation:", llm_explanations[i])
            print("Summary:", llm_summaries[i])

        # -----------------------
        # SAVE
        # -----------------------
        append_result_to_csv(file_out, [
            qid,
            emb_score,
            rerank_score,
            llm_scores[0],
            llm_scores[1],
            llm_scores[2],
            llm_scores[3],
            llm_scores[4],
        ])


